package com.hemebiotech.analytics;

/**
 * 
 * Class containing the names of input and output files
 * 
 * @author oksenhendler tristan
 *
 */
public class FileNames {
  String input;
  String output;

  /**
   * 
   * This constructor instanciate the class with the 2 files names
   *
   * @param input input file name
   * @param output output file name
   *
   */
  FileNames(String input, String output) {
    this.input = input;
    this.output = output;
  }
}
